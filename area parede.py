a = float(input("Digite a altura da parede:"))
l = float(input("Digite a largura da parede: "))
ae = a * l 
print("A area da sua parede é {} x {} sendo assim a area é {} M² ". format(a,l,ae))
tinta = ae / 2
print ("Sendo assim para pintar essa area você ira gastar {} L para pintar a parede". format(tinta))